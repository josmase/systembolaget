'use strict';

describe('Filter: openingHoursFilter', function () {

  // load the filter's module
  beforeEach(module('systembolagetApp'));

  // initialize a new instance of the filter before each test
  var openingHoursFilter;
  beforeEach(inject(function ($filter) {
    openingHoursFilter = $filter('openingHoursFilter');
  }));

  it('should return the hour span for day 2 that the store is open', function () {
    var input = '2016-04-18;10:00;19:00;;;0;_*2016-04-19;10:00;19:00;;;0;_*2016-04-20;00:00;00:00;;;-;_*2016-04-21;10:00;19:00;;;0;_*2016-04-22;10:00;19:00;;;0;_*2016-04-23;10:00;15:00;;;0;_*2016-04-24;00:00;00:00;;;Helgdag;_*2016-04-25;10:00;19:00;;;0;_*2016-04-26;10:00;19:00;;;0;_*2016-04-27;10:00;19:00;;;0;_*2016-04-28;10:00;19:00;;;0;_*2016-04-29;10:00;19:00;;;0;_*2016-04-30;10:00;15:00;;;0;_*2016-05-01;00:00;00:00;;;Helgdag;_*2016-05-02;10:00;19:00;;;0;_*2016-05-03;10:00;19:00;;;0;';
    expect(openingHoursFilter(input,1)).toBe('10:00 - 19:00');
  });
  it('should return the hour span for day 1 that the store is open', function () {
    var input = '2016-04-18;10:00;19:00;;;0;_*2016-04-19;10:00;19:00;;;0;_*2016-04-20;10:00;19:00;;;0;_*2016-04-21;10:00;19:00;;;0;_*2016-04-22;10:00;19:00;;;0;_*2016-04-23;10:00;15:00;;;0;_*2016-04-24;00:00;00:00;;;Helgdag;_*2016-04-25;10:00;19:00;;;0;_*2016-04-26;10:00;19:00;;;0;_*2016-04-27;10:00;19:00;;;0;_*2016-04-28;10:00;19:00;;;0;_*2016-04-29;10:00;19:00;;;0;_*2016-04-30;10:00;15:00;;;0;_*2016-05-01;00:00;00:00;;;Helgdag;_*2016-05-02;10:00;19:00;;;0;_*2016-05-03;10:00;19:00;;;0;';
    expect(openingHoursFilter(input,0)).toBe('10:00 - 19:00');
  });
  it('should return helgdag', function () {
    var input = '2016-04-18;10:00;19:00;;;0;_*2016-04-19;10:00;19:00;;;0;_*2016-04-20;10:00;19:00;;;0;_*2016-04-21;10:00;19:00;;;0;_*2016-04-22;10:00;19:00;;;0;_*2016-04-23;10:00;15:00;;;0;_*2016-04-24;00:00;00:00;;;Helgdag;_*2016-04-25;10:00;19:00;;;0;_*2016-04-26;10:00;19:00;;;0;_*2016-04-27;10:00;19:00;;;0;_*2016-04-28;10:00;19:00;;;0;_*2016-04-29;10:00;19:00;;;0;_*2016-04-30;10:00;15:00;;;0;_*2016-05-01;00:00;00:00;;;Helgdag;_*2016-05-02;10:00;19:00;;;0;_*2016-05-03;10:00;19:00;;;0;';
    expect(openingHoursFilter(input,6)).toBe('Helgdag');
  });
  it('should return stängt', function () {
    var input = '2016-04-18;10:00;19:00;;;0;_*2016-04-19;10:00;19:00;;;0;_*2016-04-20;10:00;19:00;;;-;_*2016-04-21;10:00;19:00;;;0;_*2016-04-22;10:00;19:00;;;0;_*2016-04-23;10:00;15:00;;;0;_*2016-04-24;00:00;00:00;;;Helgdag;_*2016-04-25;10:00;19:00;;;0;_*2016-04-26;10:00;19:00;;;0;_*2016-04-27;10:00;19:00;;;0;_*2016-04-28;10:00;19:00;;;0;_*2016-04-29;10:00;19:00;;;0;_*2016-04-30;10:00;15:00;;;0;_*2016-05-01;00:00;00:00;;;Helgdag;_*2016-05-02;10:00;19:00;;;0;_*2016-05-03;10:00;19:00;;;0;';
    expect(openingHoursFilter(input,2)).toBe('Stängt');
  });


});
